;CODE: count zero's and one's in a word
	AREA PROGRAM,CODE,READONLY
ENTRY
	LDR R0,VALUE1
	MOV R1,#0x20
	MOV R2,#0x00 ; # Of Zero's
	MOV R3,#0x00 ; # Of one's
LOOP
	MOVS R0,R0,RRX
	ADDCS R3,R3,#0x01
	ADDCC R2,R2,#0x01
	SUBS R1,R1,#0x01
	BNE LOOP
	
	AREA PROGRAM,DATA,READONLY
VALUE1 DCD &00000005
	END